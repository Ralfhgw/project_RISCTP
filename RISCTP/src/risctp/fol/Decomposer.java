  /****************************************************************************
   * After decomposition, print the generated proof trees, 
   * one tree for each theorem of the proof problem.
   * @param verbose the verbosity level of the output
   ***************************************************************************/
  public void printProofTrees(int verbose) 
  { 
    for (ProofTree tree : trees)
      tree.print(out, verbose);
  }

  /**************************************************************************
   * Determine whether a formula is not eligible for rule application.
   * @param formula a formula.
   * @return true if formula must not be transformed by rule application.
   *************************************************************************/
  private boolean isSpecial(FormulaSymbol formula)
  {
    if (problem.intAxioms.contains(formula)) return true;
    // if (problem.mapAxioms.contains(formula)) return true;
    // if (problem.dataTypeAxioms.contains(formula)) return true;
    return false;
  }

  /****************************************************************************
   * Decompose proof problem by rules of sequent calculus (propositional rules
   * and exists-left and forall-right).
   * @return a list of equivalent problems where the formulas are either
   * atomic or universal axioms or existential theorems)
   ***************************************************************************/
  public List<ProofProblem> decompose()
  {
    List<ProofProblem> problems = new ArrayList<ProofProblem>();
    List<Axiom> axioms = new ArrayList<Axiom>();
    List<Theorem> theorems = new ArrayList<Theorem>();
    List<Decl> others = new ArrayList<Decl>();
    CallDependencies dep = new CallDependencies();

    // map of symbols of axioms to their definitions
    Map<FormulaSymbol,Axiom> axiomMap = new HashMap<FormulaSymbol,Axiom>();

    // process axioms that describe definitions of predicates and functions
    for (Decl decl : problem.decls)
    {
      if (decl instanceof Theorem)
      {
        Theorem theorem = (Theorem)decl;
        theorems.add(theorem);
        continue;
      }
      if (!(decl instanceof Axiom))
      {
        others.add(decl);
        continue;
      }
      Axiom axiom = (Axiom)decl;
      FormulaSymbol symbol = (FormulaSymbol)axiom.id.getSymbol();
      axiomMap.put(symbol, axiom);
      if (isSpecial(symbol)) 
      {
        axioms.add(axiom);
        continue;
      }
      if (!expandDef)
      {
        axioms.add(axiom);
        continue;
      }
      // the axiom may characterize an implicitly defined "choice" function
      // or a function denoted by the user
      FunctionSymbol fun = problem.chooseMap.get(symbol);
      boolean isChoose = fun != null;
      if (isChoose)
      {
        Id fid = axiom.getFunId();
        if (fid != null) fun = (FunctionSymbol)fid.getSymbol();
      }
      if (fun != null)
      {
        List<VariableSymbol> vars = new ArrayList<VariableSymbol>();
        Exp formula = axiom.formula;
        int n = fun.tsymbols.size();
        for (int i = 0; i < n; i++)
        {
          if (!(formula instanceof Forall)) break; // must not happen
          Forall formula0 = (Forall)formula;
          VariableSymbol var = (VariableSymbol)formula0.tvar.id.getSymbol();
          vars.add(var);
          formula = formula0.exp;
        }
        if (isChoose)
        {
          // we may remove the existence constraint from the axiomatization 
          // of a choose function because we use this axiom only for ground
          // applications of the function for which a corresponding
          // type-checking condition has been generated
          Imp formula0 = (Imp)formula;
          formula = formula0.exp2;
        }
        // remove axiom and register definition instead
        Exp formula0 = expand(formula, axioms);
        OpDef def = new OpDef(fun, vars, formula0, symbol, axiom);
        List<OpDef> defs = implicit.get(fun);
        if (defs == null)
        {
          defs = new ArrayList<OpDef>();
          implicit.put(fun, defs);
        }
        defs.add(def);
        continue;
      }
      // attempt to transform axiom into explicit predicate/function definition
      OpDef def = defineOperation(axiom, axioms);
      if (def == null || explicit.get(def.op) != null)
      {
        axioms.add(axiom);
        continue;
      }
      // prevent expansion of (directly/indirectly) recursive definitions
      dep.add(def.op, def.exp);
      dep.close();
      if (dep.calls(def.op, def.op))
      {
        axioms.add(axiom);
        continue;
      }
      // if successful, remove axiom and register definition instead
      explicit.put(def.op, def);
    }
    
    // decompose the theorems
    for (Theorem theorem : theorems)
    {
      Sequent sequent = new Sequent(axioms, theorem);
      String name = theorem.id.toString();
      ProofTree tree = new ProofTree(name, sequent, decomposition, generation);
      if (newTree != null) newTree.accept(tree);
      trees.add(tree);
      // expansion generates new declarations
      newdecs.clear();
      tree.expand();
      List<ProofNode> leaves = getOpenLeaves(tree);
      for (ProofNode node : leaves)
      {
        ProofProblem problem0 = new ProofProblem(problem);
        problem0.setName(node.getName());
        problem0.decls = new ArrayList<Decl>(others);
        // add newly generated declarations to proof problem
        problem0.decls.addAll(newdecs);
        Sequent sequent0 = node.sequent;
        // list is modified in Completion.complete() below
        List<Formula> formulas0 = new ArrayList<Formula>(sequent0.formulas);
        for (Formula formula : formulas0)
        {
          // preserve special axioms
          Axiom axiom = axiomMap.get(formula.original);
          boolean special = isSpecial(formula.original);
          if (special) { problem0.decls.add(axiom); continue; }
          boolean negated = formula.negated;
          boolean goal = formula == sequent0.goal;
          if (!goal) negated = !negated;
          // expand defined predicate
          Exp exp = expand(formula.exp, axioms);
          // repeat arithmetic simplification of potentially expanded formula
          if (expandDef) exp = (Exp)exp.accept(arithmetic);
          // write transformed expression back into formula for later publication
          formula.exp = exp;
          if (negated) exp = Not.create(exp);
          // complete sequent by adding missing implicit function axioms
          // whenever not all instances could be created
          Completion.complete(this, node.sequent.formulas);
          Id id = Id.create(formula.name);
          FormulaSymbol symbol = new FormulaSymbol(id, !goal);
          id.setSymbol(symbol);
          if (goal)
          {
            Theorem theorem0 = Theorem.create(id, exp);
            problem0.decls.add(theorem0);
          }
          else
          {
            Axiom axiom0 = Axiom.create(id, exp);
            problem0.decls.add(axiom0);
          }
          if (!formula.original.axiom)
            problem0.originallyTheorems.add(symbol);
          if (!goal && formula.implied)
            problem0.impliedAxioms.add(symbol);
          if (problem.intAxioms.contains(formula.original)) problem.intAxioms.add(symbol);
          else if (problem.mapAxioms.contains(formula.original)) problem.mapAxioms.add(symbol);
          else if (problem.dataTypeAxioms.contains(formula.original)) problem.dataTypeAxioms.add(symbol);
        }
        // publish node after update of formula by expansion/arithmetic simplification
        if (openLeaf != null) openLeaf.accept(node);
        problems.add(problem0);
      }
    }
    return problems;
  }

  // completion of list of formulas by missing axioms
  private class Completion extends ASTVisitor.Base<Void>
  {
    // complete the list of formulas by adding those axioms
    // that characterize implicitly declared functions and
    // where not all applications in the formulas were ground
    public static void complete(Decomposer decomposer, List<Formula> formulas)
    {
      decomposer.new Completion(formulas);
    }
    
    // the implicitly defined functions of which
    // not all applications in the formulas were ground
    private Set<OpDef> defs;
    
    public Completion(List<Formula> formulas)
    {
      // determine the implicitly defined functions
      defs = new HashSet<OpDef>();
      for (Formula formula : formulas)
        formula.exp.accept(this);
      
      // add the corresponding axioms to the formulas
      Map<String,Integer> nameCounter = new HashMap<String,Integer>();
      for (OpDef def : defs)
      {
        String name = def.symbol.id.toString();
        Integer counter = nameCounter.get(name);
        if (counter == null)
          nameCounter.put(name, 0);
        else
        {
          name = name + "." + counter;
          nameCounter.put(name, counter+1);
        }
        Formula formula = new Formula(def.symbol, name, Not.create(def.axiom.formula));
        formulas.add(formula);
      }
    }
    
    public Void visit(Apply apply)
    {
      FunctionSymbol fun = (FunctionSymbol)apply.id.getSymbol();
      List<OpDef> defs0 = implicit.get(fun);
      if (defs0 == null) return null;
      Set<VariableSymbol> free = FreeVars.compute(apply);
      if (free.isEmpty()) return null;
      defs.addAll(defs0);
      return null;
    }
  }
  
  /***************************************************************************
   * Try to transform axiom into an explicit function/predicate definition.
   * @param axiom the axiom to be transformed.
   * @param axioms the list of axioms (to which new ones may be added)
   * @return the resulting definition (null, if none)
   **************************************************************************/
  private OpDef defineOperation(Axiom axiom, List<Axiom> axioms)
  {
    // determine universally quantified variables and formula body
    List<VariableSymbol> vars = new ArrayList<VariableSymbol>();
    Exp formula = axiom.formula;
    while (formula instanceof Forall)
    {
      Forall formula0 = (Forall)formula;
      VariableSymbol var = (VariableSymbol)formula0.tvar.id.getSymbol();
      vars.add(var);
      formula = formula0.exp;
    }
    
    // determine both sides of of equivalence or equality
    Exp exp1; Exp exp2;
    if (formula instanceof Equiv)
    {
      Equiv formula0 = (Equiv)formula;
      exp1 = formula0.exp1;
      exp2 = formula0.exp2;
    }
    else if (formula instanceof Apply)
    {
      Apply formula0 = (Apply)formula;
      FunctionSymbol pred = (FunctionSymbol)formula0.id.getSymbol();
      if (!eqpreds.contains(pred)) return null;
      exp1 = formula0.exps[0];
      exp2 = formula0.exps[1];
    }
    else return null;
    
    // determine predicate/function symbol in left-hand-side application
    if (!(exp1 instanceof Apply)) return null;
    Apply apply = (Apply)exp1;
    FunctionSymbol op = (FunctionSymbol)apply.id.getSymbol();

    // check that arguments of predicate match the universally quantified variables
    Exp[] args = apply.exps;
    int n = args.length;
    if (n != vars.size()) return null;
    for (int i = 0; i < n; i++)
    {
      Exp arg = args[i];
      if (!(arg instanceof Var)) return null;
      Var arg0 = (Var)arg;
      VariableSymbol var = vars.get(i);
      if (var != arg0.id.getSymbol()) return null;
    }
    
    // expand the function/predicate applications in the defining expression
    Exp exp0 = expand(exp2, axioms);
    FormulaSymbol symbol = (FormulaSymbol)axiom.id.getSymbol();
    return new OpDef(op, vars, exp0, symbol, axiom);
  }
  
  /***************************************************************************
   * Indent output for given nesting level.
   * @param level the nesting level.
   * @param out the output stream
   **************************************************************************/
  private static void tab(int level, PrintWriter out)
  {
    for (int i = 0; i < level; i++) out.print("  ");
  }

  /****************************************************************************
   * Get open leaves of tree in dept-first left-to-right order.
   * @param tree the tree.
   * @return the open leaves of the tree.
   ***************************************************************************/
  private static List<ProofNode> getOpenLeaves(ProofTree tree)
  {
    List<ProofNode> result = new ArrayList<ProofNode>();
    addOpenLeaves(tree.root, result);
    return result;
  }
  
  /****************************************************************************
   * Add to list the open leaves of the given subtree.
   * @param node the root of the subtree.
   * @param result the list to which the open leaves are added.
   ***************************************************************************/
  private static void addOpenLeaves(ProofNode node, List<ProofNode> result)
  {
    List<ProofNode> children = node.children;
    if (children == null) { result.add(node); return; }
    for (ProofNode child : children)
      addOpenLeaves(child, result);
  }
  
