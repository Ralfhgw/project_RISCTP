  // the problem to which the prover is applied.
  private ProofProblem problem;

  // the equality and inequality predicates of the problem
  private Collection<FunctionSymbol> eqpreds;
  private Collection<FunctionSymbol> neqpreds;
  
  // various integer operations
  private FunctionSymbol equalPred;
  private FunctionSymbol notEqualPred;
  private FunctionSymbol lessPred;
  private FunctionSymbol lessEqualPred;
  private FunctionSymbol zeroConst;
  private FunctionSymbol addFun;
  
  // the output stream to be used for printing
  private PrintWriter out;

  // expand predicate/function definitions in decomposition
  private boolean expandDef;
  
  // the list of new declarations generated by the prover
  private List<Decl> newdecs;

  // the proof trees generated by the prover
  private List<ProofTree> trees;

  // the decomposition and the knowledge generation rules to be applied by the prover
  private List<ProofRule> decomposition;
  private List<ProofRule> generation;
  
  // if not null, methods to be invoked on new proof trees and 
  // on parent nodes of new children and on open leaf nodes
  private Consumer<ProofTree> newTree;
  private Consumer<ProofNode> newChildren;
  private Consumer<ProofNode> openLeaf;

  // a map of explicitly defined predicates/functions 
  // to their defining formulas/expressions (if any)
  private Map<FunctionSymbol,OpDef> explicit;
  
  // a map of implicitly defined functions to their defining formulas
  private Map<FunctionSymbol,List<OpDef>> implicit;

  // the definition of a function/predicate by an expression/formula 
  // derived from an axiom
  private static class OpDef
  {
    public final FunctionSymbol op;
    public final List<VariableSymbol> vars;
    public final Exp exp;
    public final FormulaSymbol symbol;
    public final Axiom axiom;
    public OpDef(FunctionSymbol op, List<VariableSymbol> vars, Exp exp,
      FormulaSymbol symbol, Axiom axiom)
    {
      this.op = op;
      this.vars = vars;
      this.exp = exp;
      this.symbol = symbol;
      this.axiom = axiom;
    }
  }
  
  // the arithmetic simplifier
  private Arithmetic arithmetic;
  
  // the current proof node being expanded
  private ProofNode currentProofNode = null;
  
  /****************************************************************************
   * Create an instance of the sequent calculus prover for the stated problem.
   * @param problem the proof problem to which the prover is applied.
   * @param out the stream to be used for output.
   * @param expandDef expand predicate and function definitions.
   * @param newTree method to be invoked on new proof trees (may be null).
   * @param newChildren method to be invoked on parents of new children (may be null).
   * @param openLeaf method to be invoked on open leaf nodes (may be null).
   ***************************************************************************/
  public Decomposer(ProofProblem problem, PrintWriter out,
    boolean expandDef,
    Consumer<ProofTree> newTree, Consumer<ProofNode> newChildren,
    Consumer<ProofNode> openLeaf)
  {
    this.problem = problem;
    this.eqpreds = problem.equalities.values();
    this.neqpreds = problem.inequalities.values();
    this.out = out;
    this.expandDef = expandDef;
    this.newdecs = new ArrayList<Decl>();
    this.trees = new ArrayList<ProofTree>();
    this.newTree = newTree;
    this.newChildren = newChildren;
    this.openLeaf = openLeaf;
    this.explicit = new HashMap<FunctionSymbol,OpDef>();
    this.implicit = new HashMap<FunctionSymbol,List<OpDef>>();
    this.arithmetic = new Arithmetic(problem);
    
    // gather relevant theory symbols
    equalPred = problem.equalities.get(problem.intSymbol);
    notEqualPred = problem.inequalities.get(problem.intSymbol);
    lessPred = null;
    lessEqualPred = null;
    addFun = null;
    for (FunctionSymbol fun : problem.intFuns)
    {
      String name = fun.id.toString();
      switch (name)
      {
      case AST.LESSNAME: lessPred = fun; break;
      case AST.LESSEQUALNAME: lessEqualPred = fun; break;
      case "0": zeroConst = fun; break;
      case AST.PLUSNAME: addFun = fun; break;
      }
    }
    
    // the decomposition rules 
    decomposition = new ArrayList<ProofRule>();

    // first simplify formulas
    decomposition.add(new SimplifyRule());
    decomposition.add(new RelationRule());

    // apply the rules of the sequent calculus that do not split the proof situation
    decomposition.add(new AxiomRule());
    decomposition.add(new TrueRightFalseLeftRule());
    decomposition.add(new FalseRightTrueLeftRule());
    decomposition.add(new AndLeftOrRightImpRightRule());
    decomposition.add(new EquivLeftRightRule());
    decomposition.add(new ExistsLeftForallRightRule());
    
    // handle equality and non-equality
    decomposition.add(new EqualRule());
    decomposition.add(new NotEqualRule());
    
    // expand definitions and substitute constants
    if (expandDef) decomposition.add(new DefRule());
    decomposition.add(new DefEqualRule());
    
    // apply the McCarthy array rule
    decomposition.add(new PutGetRule());
     
    // the knowledge generation rules
    generation = new ArrayList<ProofRule>();

    // perform case distinction
    generation.add(new AndRightOrLeftImpLeftRule());
   
    // generate new knowledge from inequalities
    generation.add(new LessEqualRule());
    generation.add(new BoundedRule());
    // generation.add(new LessImpNotEqualRule());
    // generation.add(new EqualImpLessEqualRule());
    generation.add(new PlusOneRule());
    /*
    generation.add(new TransitivityRule("trans", 
        ((FunctionSymbol pred, Exp exp1, Exp exp2)->
        pred == lessPred || pred == lessEqualPred), 
        ((FunctionSymbol pred, Exp exp1, Exp exp2)->
        {
          // avoid generation of literal t1 ~ t2+1 which would lead 
          // by application of PlusOneSplitRule to an infinite cycle
          if (pred != lessPred && pred != lessEqualPred) return false;
          return matchPlusOneExp(exp2) == null;
        }),
        ((FunctionSymbol pred1, FunctionSymbol pred2)->
        (pred1 == lessEqualPred && pred2 == lessEqualPred) ? 
            lessEqualPred : lessPred)));
    */
    
    // as a last resort, apply an arithmetic case split
    generation.add(new PlusOneSplitRule());
  }

